package com.example.babycareco225;

public class DataPoint {
    public DataPoint(int xValue, int yValue) {
        this.xValue = xValue;
        this.yValue = yValue;
    }

    int xValue;

    public DataPoint() {

    }

    public int getxValue() {
        return xValue;
    }

    public int getyValue() {
        return yValue;
    }

    int yValue;



}
